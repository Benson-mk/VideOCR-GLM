const fs = require('fs');
const path = require('path');

// Read version from package.json
function getVersion(packageJsonPath) {
  try {
    const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'));
    return packageJson.version || 'unknown';
  } catch (error) {
    console.error(`Error reading ${packageJsonPath}:`, error.message);
    return 'unknown';
  }
}

// Get CLI version from Python file
function getCliVersion() {
  try {
    const cliPath = path.join(__dirname, '..', 'VideOCR-GLM-CLI', 'videocr_glm_cli.py');
    const content = fs.readFileSync(cliPath, 'utf8');
    
    // Look for __version__ variable
    const versionMatch = content.match(/__version__\s*=\s*['"]([^'"]+)['"]/);
    if (versionMatch) {
      return versionMatch[1];
    }
    
    // If no version found, return a default or read from requirements.txt
    return '1.0.1'; // Default version
  } catch (error) {
    console.error('Error reading CLI version:', error.message);
    return 'unknown';
  }
}

// Generate versions
const versions = {
  buildVersion: getVersion(path.join(__dirname, '..', 'package.json')),
  frontendVersion: getVersion(path.join(__dirname, '..', 'frontend', 'package.json')),
  backendVersion: getVersion(path.join(__dirname, '..', 'backend', 'package.json')),
  cliVersion: getCliVersion(),
  buildDate: new Date().toISOString(),
};

// Generate TypeScript file content
const tsContent = `// Auto-generated by scripts/generate-versions.cjs
// DO NOT EDIT MANUALLY

export const versions = {
  buildVersion: '${versions.buildVersion}',
  frontendVersion: '${versions.frontendVersion}',
  backendVersion: '${versions.backendVersion}',
  cliVersion: '${versions.cliVersion}',
  buildDate: '${versions.buildDate}',
} as const;

export type Versions = typeof versions;
`;

// Write to frontend src directory
const outputPath = path.join(__dirname, '..', 'frontend', 'src', 'versions.ts');
fs.writeFileSync(outputPath, tsContent, 'utf8');

console.log('[OK] Versions generated successfully:');
console.log(`   Build Version: ${versions.buildVersion}`);
console.log(`   Frontend Version: ${versions.frontendVersion}`);
console.log(`   Backend Version: ${versions.backendVersion}`);
console.log(`   CLI Version: ${versions.cliVersion}`);
console.log(`   Build Date: ${versions.buildDate}`);
console.log(`\nWritten to: ${outputPath}`);